public class Arithmatic {
    public static void main(String[] args) {
        int A = 12;
        int B = 24;
        System.out.println("Add = "+ (A+B));
        System.out.println("Sub = "+ (A-B));
        System.out.println("Mult = "+ (A*B));
        System.out.println("Div = "+ (B/A));
        System.out.println("Mod = "+ (B%A));
    }
}