public class Relational {
    public static void main(String[] args) {
        int A = 19;
        int B = 25;
        System.out.println(A);
        System.out.println(B);
        System.out.println(A==B);
        System.out.println(A!=B);
        System.out.println(A>=B);
        System.out.println(A<=B);
    }
}
